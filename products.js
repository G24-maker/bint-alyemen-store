function getNewProducts(products, hours = 24) {
    const now = new Date();
    const cutoffTime = new Date(now.getTime() - (hours * 60 * 60 * 1000));
    
    return products.filter(p => {
        const productDate = new Date(p.createdAt);
        return productDate >= cutoffTime;
    });
}

